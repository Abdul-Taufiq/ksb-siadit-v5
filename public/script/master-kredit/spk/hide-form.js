function hideForm1() {
    var konten = document.getElementById("konten1");
    var show = document.getElementById("show1");

    // Jika elemen tidak memiliki class d-none (default terlihat)
    if (konten.classList.contains("showing")) {
        show.classList.add("fa-eye-slash");
        show.classList.remove("fa-eye");
        konten.classList.add("d-none");
        konten.classList.remove("showing");
    } else {
        show.classList.remove("fa-eye-slash");
        show.classList.add("fa-eye");
        konten.classList.remove("d-none");
        konten.classList.add("showing");
    }
}

